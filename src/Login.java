    private void btnloginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnloginActionPerformed
     //Logica para la validacion del usuario   
     PreparedStatement ps;
     ResultSet rs;
     
     String usuario = txtusuariologin.getText();
     String contra = String.valueOf(txtcontralogin.getPassword());
     
  
      //Query para hacer el select de los datos en la bd
      String query = "SELECT * FROM `usuarios` WHERE `u_nombreusuario` = ? AND `u_contra` = ?";
      
        try {
            ps = MyConexion.Conectar().prepareStatement(query);
            
            //Pasando cada uno de los parametros para validar el usuario 
            ps.setString(1, usuario);
            ps.setString(2, contra);
            
            rs = ps.executeQuery();
             
            //Validar el usuario para entrar en la pantalla principal
            if (rs.next()) {
               PantallaPrincipal pp = new PantallaPrincipal();
               pp.setVisible(true);
               pp.pack();
               pp.setLocationRelativeTo(null);
              JOptionPane.showMessageDialog(null, "BIENVENIDO AL SISTEMA");
              this.dispose();
            }
            else if (usuario.equals("")) {
            JOptionPane.showMessageDialog(null, "Debes ingresar un Usuario");
            }
            else if (contra.equals("")) {
                JOptionPane.showMessageDialog(null, "Debes ingresar una Contrase√±a");
            }
            else {
                 JOptionPane.showMessageDialog(null, "Usuario no Registrado, Debe Registrarse");   
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
     
     
    }//GEN-LAST:event_btnloginActionPerformed

